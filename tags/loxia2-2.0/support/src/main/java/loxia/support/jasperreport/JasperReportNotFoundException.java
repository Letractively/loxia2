package loxia.support.jasperreport;

public class JasperReportNotFoundException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = -8100024230444541223L;
	
	public JasperReportNotFoundException() {
		super();
	}

	public JasperReportNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public JasperReportNotFoundException(String message) {
		super(message);
	}

	public JasperReportNotFoundException(Throwable cause) {
		super(cause);
	}	

}
